# 商品列表 - 这里定义了我们有哪些产品需要生成图片提示词
# 就像是一个购物清单，里面列出了5种不同的商品
products = ['运动鞋', '不锈钢保温杯', '蓝牙耳机', 'LED台灯', '瑜伽垫']

# 背景风格列表 - 定义了每种产品可以搭配的不同拍摄背景
# 就像是给产品选择不同的拍照场景，比如在家里拍、在户外拍等
styles = ['简约白色背景', '户外自然场景', '暖色家居环境', '科技感光影', '健身房使用场景']

special_products = ['蓝牙耳机','LED台灯']

# 存放所有prompt的列表 - 创建一个空盒子，用来装后面生成的所有提示词
# 刚开始是空的，后面会往里面添加内容
prompts = []

# 双重循环生成组合 - 这是核心部分，让每个产品都和每个背景搭配一次
# 想象一下：拿第一个产品（运动鞋），依次和5个背景都搭配一遍
# 然后拿第二个产品（保温杯），再依次和5个背景都搭配一遍
# 以此类推...这样就能得到很多不同的组合
for product in products:
    for style in styles:
        # f-string是一种格式化字符串的方法，可以在字符串里直接插入变量
        # 这行代码把产品和背景组合成一句话，作为AI绘画的提示词

        # 特殊处理：如果是"运动鞋"在"健身房"场景，使用特殊的描述
        # 这样可以针对特定组合定制更精准的提示词
        # if product == '运动鞋' and style == '健身房使用场景':
        #     # 运动鞋在健身房需要更有动感，所以加了"动态抓拍"和"超高清"
        #     prompt = f"一张{product}的产品摄影图，{style}，超高清，动态抓拍，商业摄影"
        # else:
        #     # 其他所有组合都使用标准的描述模板
        #     prompt = f"一张{product}的产品摄影图，{style}，高清，商业摄影"
        prompt = f"一张{product}的产品摄影图，{style}，高清，商业摄影"
        if product in special_products and style == '科技感光影':
            prompt = prompt + "，带发光特效"

        # 把生成好的提示词添加到我们的"盒子"（prompts列表）里
        # append()方法就像往盒子里放东西，每次放一个
        prompts.append(prompt)


# 打印前5条检查 - 先显示前5个结果，看看生成的对不对
# enumerate函数可以同时获得索引(i)和值(p)，i从0开始，所以i+1让它从1开始显示
for i,p in enumerate(prompts[:5]):
    print(f"{i+1}:{p}")

# 最后告诉用户一共生成了多少条提示词
# len()函数用来计算列表里有多少个元素
print(f"\n一共生成了{len(prompts)}条prompt")

# ========== 以下是新增的文件保存功能 ==========

# 打开（或创建）一个名为 prompts.txt 的文件
# 'w' 表示写入模式（write）：如果文件不存在就创建，如果存在就清空重写
# encoding='utf-8' 表示使用UTF-8编码，这样才能正确保存中文
# as f 表示给这个文件起个别名叫 f，后面用 f 来操作它
with open('prompts.txt', 'w', encoding='utf-8') as f:
    # 遍历所有的提示词
    for p in prompts:
        # 把每条提示词写入文件
        # + "\n" 表示在每个提示词后面加一个换行符
        # 这样每条提示词都会单独占一行，方便查看和使用
        f.write(p + "\n")

# 文件保存完成后，打印提示信息告诉用户
# with语句结束后，文件会自动关闭，不需要手动调用close()
print("prompts.txt 已保存")